# In this code we discuss about arithmetic operations 

num1=int(input("Enter first number: "))
num2=int(input("Enter second number: "))

# here "int" stands for input type should be integer
# "input()" stands for reading input from the user

# Addition of two numbers
add=num1+num2
print(add)
# Substraction of two numbers 
sub=num1-num2
print(sub)
# Multiplication of two numbers
mult=num1*num2
print(mult)
# Division of two numbers
div=num1//num2
# here we use // to get integer value 
# if we use / we get floating value
print(div)
